# Vamos receber dois dados diferentes do usuário e concatena-los em uma única string?!

info1 = input('Informe a primeira informação: ')
info2 = input('Informa a segunda informação: ')

info_concatenado = info1 + ' ' + info2

print(f'Com a primeira informação ({info1}) e a segunda informção ({info2}) o resutado será --> {info_concatenado}.')